n=input("Введите четырехзначное число: ")
if len(n)!=4: print("Вы ввели не четырехзначное число!")
else:
  # 1) число, полученное при прочтении его цифр справа налево
  rev_number = int(n[::-1])
  print("Число, полученное при прочтении его цифр справа налево: ", rev_number)

  # 2) число, образуемое при перестановке первой и второй, третьей и четвертой цифр заданного числа
  swap_12_34_number = int(n[1] + n[0] + n[3] + n[2])
  print("Число, образуемое при перестановке первой и второй, третьей и четвертой цифр заданного числа: ",\
        swap_12_34_number)

  # 3) число, образуемое при перестановке второй и третьей цифр заданного числа
  swap_23_number = int(n[0] + n[2] + n[1] + n[3])
  print("Число, образуемое при перестановке второй и третьей цифр заданного числа: ",\
        swap_23_number)

  # 4) число, образуемое при перестановке двух первых и двух последних цифр заданного числа
  # неарифметический способ
  swap_first_last_number = int(n[2:] + n[:2])
  print("Число, образуемое при перестановке двух первых и двух последних цифр заданного числа (способ 1): ",\
        swap_first_last_number)
  #арифметический способ 1 (с выделением цифр числа)
  ones = int(n) % 10 #единицы
  thds = int(n) // 1000 #тысячи
  hdrs = (int(n) % 1000) // 10
  tens = hdrs % 10 #десятки
  hdrs = hdrs // 10 #сотни
  swap_first_last_number1 = tens*1000 + ones*100+thds*10+hdrs
  print("Число, образуемое при перестановке двух первых и двух последних цифр заданного числа (способ 2):",\
        swap_first_last_number1)

  #арифметический способ 2 без выделения цифр числа
  swap_first_last_number2 = (int(n) % 100) * 100 + (int(n) // 100)
  print("Число, образуемое при перестановке двух первых и двух последних цифр заданного числа (способ 2):",\
        swap_first_last_number2)